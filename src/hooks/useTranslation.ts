export const useTranslation = () => {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('preferredLanguage');
      return (saved as Language) || 'en';
    }
    return 'en';
  });

  useEffect(() => {
    localStorage.setItem('preferredLanguage', language);
  }, [language]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const t = (key: string, fallback?: string): string => {
    // Try to get from current language
    const translation = translations[language]?.[key];
    if (translation) return translation;
    
    // Fall back to English
    const englishTranslation = translations.en?.[key];
    if (englishTranslation) return englishTranslation;
    
    // Use provided fallback or extract key name
    if (fallback) return fallback;
    
    // Extract readable text from key (e.g., 'nav.home' -> 'Home')
    const parts = key.split('.');
    const lastPart = parts[parts.length - 1];
    return lastPart.charAt(0).toUpperCase() + lastPart.slice(1).replace(/([A-Z])/g, ' $1');
  };

  // Helper function to get all available translation keys
  const getAvailableKeys = (): string[] => {
    return Object.keys(translations.en);
  };

  // Helper function to check if a key exists
  const hasTranslation = (key: string): boolean => {
    return !!translations[language]?.[key] || !!translations.en?.[key];
  };

  return { language, setLanguage, t };
};

export default useTranslation;
